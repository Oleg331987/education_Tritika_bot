            caption += f"<b>{module['title']}</b>\n\n"
            caption += f"‚è± <b>–î–ª–∏—Ç–µ–ª—å–Ω–æ—Å—Ç—å:</b> {audio_info['duration']//60}:{audio_info['duration']%60:02d}\n"
